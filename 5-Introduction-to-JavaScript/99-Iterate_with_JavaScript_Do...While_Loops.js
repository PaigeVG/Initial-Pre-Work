// do... while is a type of loop that will execute a piece of code under the "do" once before it launches into the "while". A normal while would not do anything if its condition evaluated to false on the first loop.

// Setup
var myArray = [];
var i = 10;

// Only change code below this line.

do {
  myArray.push(i);
  i++;
} while (i < 5);

